1. Type de forêt tropicale adaptée :

Pourquoi une forêt humide ?
Elle peut être recréée dans une serre ou un enclos climatisé.
Les visiteurs apprécieront l'expérience immersive dans un environnement dense, vert et chaleureux, contrastant avec le climat breton.


3. Animaux à intégrer :
Mammifères :
Petits primates : ouistitis, tamarins, singes capucins.
Paresseux (très populaires pour les forêts tropicales).

Oiseaux :
Toucans, perroquets, aras.
Flamants roses ou ibis, si une zone humide est intégrée.

Reptiles et amphibiens :
Grenouilles dendrobates colorées.
Serpents tropicaux (boa, python).
Tortues d'eau douce.

Insectes et poissons :
Papillons tropicaux dans une zone dédiée (ex. volière à papillons).
Bassins avec des poissons exotiques (comme des piranhas ou des discus).


4. Expérience pour les visiteurs :
Immersion :
Sentiers suspendus : Installer des passerelles au niveau de la canopée pour permettre aux visiteurs d’observer les animaux dans les hauteurs.
Éducation interactive : Des panneaux explicatifs sur la biodiversité tropicale, la déforestation et la conservation.
Zones d’observation :
Des abris vitrés pour observer les animaux sans les déranger.
Une zone d’exploration aquatique avec des bassins vitrés pour voir la faune sous-marine.

